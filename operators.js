//Arithmatic operators
let a=50
let b=5

console.log(" a + b = ",a+b)
console.log(" a - b = ",a-b)
console.log(" a * b = ",a*b)
console.log(" a / b = ",a/b)

console.log(" a ** b = ",a**b) //exponential

console.log(" a % b = ",a%b) //modulus

console.log(" a ++ = ",a++) //post increment
console.log(" a -- = ",a--) //post decrement

console.log(" ++a = ",++a) //pre increment
console.log(" --a = ",--a) //pre decrement

